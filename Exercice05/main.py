def sum(a, b):
    """
    Does the addition of number a and b
    :param a: first argument
    :param b: second argument
    :return: the sum of a and b
    """
    return a + b
 
def subtraction(a, b):
    """
    Does the subtraction of number a and b
    :param a: first argument
    :param b: second argument
    :return: the subtraction of a and b
    """
    return a - b
